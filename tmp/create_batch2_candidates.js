const byId = new Map(inventory.map((item) => [item.id, item]));
const rows = candidates.map((candidate) => {
  const image = byId.get(candidate.imageId) || {};
  return {
    ...candidate,
    sourceFilename: image.name || "",
    sourcePath: image.sourcePath || "",
  };
});

const headers = [
  "imageId",
  "sourceFilename",
  "visualName",
  "blingId",
  "sku",
  "productName",
  "price",
  "stock",
  "desiredFileName",
  "status",
  "action",
  "notes",
  "sourcePath",
];

function csvEscape(value) {
  const text = String(value ?? "");
  return /[;"\n\r]/.test(text) ? `"${text.replace(/"/g, '""')}"` : text;
}

fs.writeFileSync(
  path.join(REVIEW_DIR, "batch2-association-candidates.csv"),
  "\uFEFF" + [headers.join(";"), ...rows.map((row) => headers.map((key) => csvEscape(row[key])).join(";"))].join("\n"),
  "utf8"
);

